package models

import (
	"fmt"

	"github.com/jinzhu/gorm"
)

/*
Server is xcjsjcghj
*/
type Server struct {
	gorm.Model
	IP   string
	Name string
}

/*
MigrateDB migrates all schemas in database
*/
func MigrateDB() {
	getDB().AutoMigrate(&Server{})
}

/*
Create is for creation of new records for server
*/
func Create(IP string, Name string) {
	getDB().Create(&Server{IP: IP, Name: Name})
}

/*
PaintServers prints servers
*/
func PaintServers() {
	var server Server
	getDB().Find(&server)
	fmt.Printf("Hello %v\n", server)
}
